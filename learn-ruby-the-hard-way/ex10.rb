# Here are a few things to be mindful of when printing strings

tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\nona line."
backslash_cat = "I'm \\ a \\ cat"

fat_cat = """
I'll do a list: 
\t* Cat food
\t* Fish
\t* Catnip\n\t*Grass
"""

puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat

